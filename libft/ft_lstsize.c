#include"libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*cru;

	i = 0;
	cru = lst;
	while (cru != NULL)
	{
		i++;
		cru = cru->next;
	}
	return (i);
}
